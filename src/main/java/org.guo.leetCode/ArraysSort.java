package org.guo.leetCode;

import java.util.Arrays;

public class ArraysSort {

    public static void main(String[] args) {
        int[] nums = {4,6,2,8,0,4,9};
        Arrays.sort(nums);
        for (int i = 0; i < nums.length; i++) {
            System.out.println(nums[i]);
        }
        Integer.bitCount(2);
    }
}
